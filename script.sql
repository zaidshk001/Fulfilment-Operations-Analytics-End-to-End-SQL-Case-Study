-- Q45.	What is biggest cost driver in fulfilment pipeline?
-- =========================================================

'In your dataset, cost can be approximated by:
processing_time × operational cost rate
Since we don’t have labor cost per stage, the best proxy is:
Total processing hours spent in each stage
The stage consuming the most total time is likely the biggest cost driver.'

WITH stage_time AS (
SELECT
    stage_name,
    EXTRACT(EPOCH FROM (adjusted_stage_end_time - stage_start_time))/3600 AS processing_hours
FROM ops.fulfilment_events_clean
WHERE adjusted_stage_end_time IS NOT NULL
)

SELECT
stage_name,
COUNT(*) AS total_events,
ROUND(AVG(processing_hours),2) AS avg_processing_hours,
ROUND(SUM(processing_hours),2) AS total_processing_hours
FROM stage_time
GROUP BY stage_name
ORDER BY total_processing_hours DESC;

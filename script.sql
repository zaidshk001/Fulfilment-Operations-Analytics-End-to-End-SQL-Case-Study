-- =========================================================
-- Q4. Which stage contributes most to SLA breaches?
-- Calculates stage-level SLA violations
-- =========================================================

	WITH stage_durations AS (
	    SELECT
	        f.stage_name,
	        EXTRACT(EPOCH FROM 
	            (f.adjusted_stage_end_time - f.stage_start_time)
	        ) / 3600 AS stage_hours,
	        s.sla_hours
	    FROM fulfilment_events_clean f
	    JOIN sla s
	        ON f.stage_name = s.stage_name
	    WHERE f.adjusted_stage_end_time IS NOT NULL
	),
	
	breach_flags AS (
	    SELECT
	        stage_name,
	        CASE 
	            WHEN stage_hours > sla_hours THEN 1
	            ELSE 0
	        END AS breach_flag
	    FROM stage_durations
	)
	
	SELECT
	    stage_name,
	    SUM(breach_flag) AS total_breaches,
	    ROUND(
	        SUM(breach_flag) * 100.0 / 
	        SUM(SUM(breach_flag)) OVER (), 2
	    ) AS contribution_percentage
	FROM breach_flags
	GROUP BY stage_name
	ORDER BY total_breaches DESC;;




-- =========================================================
-- Handling Missing Stage End Times & Event Cleaning
-- This section fixes missing timestamps and creates a clean
-- operational events table for SLA and duration analysis.
-- =========================================================


-- =========================================================
-- Create a view to handle missing stage_end_time values
-- Strategy:
-- If stage_end_time is NULL, use the start time of the next stage
-- =========================================================

CREATE VIEW null_events_handled AS
WITH ordered_events AS (
    SELECT
        order_id,
        stage_name,
        stage_start_time,
        stage_end_time,
        status,
        hub_id,
        LEAD(stage_start_time) OVER (
            PARTITION BY order_id
            ORDER BY stage_start_time
        ) AS next_stage_start_time      -- next stage start time per order
    FROM fulfilment_events
)

SELECT
    order_id,
    stage_name,
    stage_start_time,
    CASE
        WHEN stage_end_time IS NOT NULL THEN stage_end_time
        WHEN stage_end_time IS NULL
             AND next_stage_start_time IS NOT NULL
        THEN next_stage_start_time      -- impute missing end time
        ELSE NULL
    END AS adjusted_stage_end_time,
    status,
    hub_id
FROM ordered_events;


-- =========================================================
-- Validate that missing end times were handled correctly
-- =========================================================

SELECT *
FROM null_events_handled
WHERE adjusted_stage_end_time IS NULL;


-- Check for invalid negative durations
SELECT *
FROM null_events_handled
WHERE adjusted_stage_end_time < stage_start_time;


-- =========================================================
-- Create a materialized view for cleaned fulfilment events
-- Materialized view improves performance for repeated analysis
-- =========================================================

CREATE MATERIALIZED VIEW fulfilment_events_clean AS
WITH ordered_events AS (
    SELECT
        order_id,
        stage_name,
        stage_start_time,
        stage_end_time,
        status,
        hub_id,
        LEAD(stage_start_time) OVER (
            PARTITION BY order_id
            ORDER BY stage_start_time
        ) AS next_stage_start_time
    FROM fulfilment_events
)

SELECT
    order_id,
    stage_name,
    stage_start_time,
    CASE
        WHEN stage_end_time IS NOT NULL THEN stage_end_time
        WHEN stage_end_time IS NULL
             AND next_stage_start_time IS NOT NULL
        THEN next_stage_start_time
        ELSE NULL
    END AS adjusted_stage_end_time,
    status,
    hub_id
FROM ordered_events;



-- =========================================================
-- Fulfilment Operations Data Model Setup
-- This section creates the schema and core operational tables
-- used throughout the fulfilment analytics project.
-- =========================================================

-- Create dedicated schema for operations data
CREATE SCHEMA ops;

-- =========================================================
-- ORDERS TABLE
-- Stores core order-level information used for SLA and
-- fulfilment performance analysis
-- =========================================================

CREATE TABLE IF NOT EXISTS ops.orders (
    order_id BIGINT PRIMARY KEY,              -- unique order identifier
    order_date DATE,                          -- order placement date
    customer_country VARCHAR(100),            -- customer location
    destination_country VARCHAR(100),         -- delivery destination
    hub_id INT,                               -- fulfilment hub processing the order
    product_category VARCHAR(100),            -- product category
    order_value NUMERIC(10,2),                -- revenue generated by the order
    priority_flag VARCHAR(20),                -- indicates express/priority orders
    promised_delivery_date DATE               -- SLA commitment for delivery
);


-- =========================================================
-- FULFILMENT EVENTS TABLE
-- Tracks order progression across fulfilment stages
-- =========================================================

CREATE TABLE IF NOT EXISTS ops.fulfilment_events (
    event_id BIGSERIAL PRIMARY KEY,           -- unique event identifier
    order_id BIGINT,                          -- associated order
    stage_name VARCHAR(100),                  -- fulfilment stage (Intake, Packing, QC, Shipping, Delivery)
    stage_start_time TIMESTAMP,               -- stage start timestamp
    stage_end_time TIMESTAMP,                 -- stage completion timestamp
    status VARCHAR(50),                       -- stage status
    hub_id INT,                               -- hub processing this stage
    FOREIGN KEY (order_id) REFERENCES ops.orders(order_id)  -- maintain referential integrity
);


-- =========================================================
-- HUBS TABLE
-- Contains metadata about fulfilment hubs and their capacity
-- =========================================================

CREATE TABLE ops.hubs (
    hub_id INT PRIMARY KEY,                   -- unique hub identifier
    hub_name VARCHAR(50),                     -- hub name
    hub_country VARCHAR(10),                  -- country where hub is located
    capacity_per_day INT                      -- max orders the hub can process daily
);


-- =========================================================
-- SHIPPING TABLE
-- Stores logistics partner details and shipment cost
-- =========================================================

CREATE TABLE ops.shipping (
    shipped_id BIGSERIAL PRIMARY KEY,         -- shipment record id
    order_id INT,                             -- associated order
    shipping_partner VARCHAR(50),             -- logistics provider
    shipping_zone VARCHAR(10),                -- delivery region classification
    weight_bucket VARCHAR(20),                -- shipment weight category
    shipping_cost NUMERIC(10,2),              -- logistics cost per shipment
    FOREIGN KEY (order_id) REFERENCES ops.orders(order_id)
);


-- =========================================================
-- RETURNS TABLE
-- Tracks returned orders and refund information
-- =========================================================

CREATE TABLE IF NOT EXISTS ops.returns (
    return_id BIGSERIAL PRIMARY KEY,          -- return record identifier
    order_id INT,                             -- returned order
    return_reason VARCHAR(50),                -- reason for return
    return_request_date DATE,                 -- return request date
    refund_amount NUMERIC(10,2),              -- refunded amount
    FOREIGN KEY (order_id) REFERENCES ops.orders(order_id)
);


-- =========================================================
-- SLA TABLE
-- Defines stage-level service level agreements
-- =========================================================

CREATE TABLE IF NOT EXISTS ops.sla (
    stage_name VARCHAR(20),                   -- fulfilment stage
    sla_hours INT                             -- SLA threshold in hours
);

